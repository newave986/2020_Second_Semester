public class CircleArea {

    public static void main(String[] args) {
        final double PI = 3.14; // define the circumference rate as a constant

        double radius = 10.0; // radius of circle
        double circleArea = radius*radius*PI; // calculate the area of circle

        System.out.println("Area of Given Circle = " + circleArea);
        // print the area of circle out to screen
    }
}